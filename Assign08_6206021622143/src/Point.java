public class Point {
    private int x, y, height, width;

    public void set_x(int x) {
        this.x = x;
    }

    public int get_x() {
        return this.x;
    }

    public void set_y(int y) {
        this.y = y;
    }

    public int get_y() {
        return this.y;
    }

    public void set_width(int w) {
        this.width = w;
    }

    public int get_width() {
        return this.width;
    }

    public void set_height(int h) {
        this.height = h;
    }

    public int get_height() {
        return this.height;
    }

}
