            public class Point {

                private int x = 0, y = 0, speed = 0;

                public void setX(int x) {
                    this.x = x;
                }

                public void setY(int y) {
                    this.y = y;
                }

                public void setSpeed(int speed) {
                    this.speed = speed;
                }

                public int getX() {
                    return this.x;
                }

                public int getY() {
                    return this.y;
                }

                public int getSpeed() {
                    return this.speed;
                }
}